    public static int binarySearch(int[] nums, int target) {
        //write your code here

        if(nums == null || nums.length == 0){
            return -1;
        }

        int start = 0;
        int end = nums.length - 1;

        while(start + 1 < end){
            int mid = start + (end - start) / 2;

            if(nums[start] == target){
                return start;
            }

            if(nums[mid] >= target){
                end = mid;
            }else {
                start = mid;
            }

        }

        if(nums[start] == target){
            return start;
        }

        if(nums[end] == target){
            return end;
        }


        return -1;
    }

